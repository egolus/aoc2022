def solve_a(data):
    tube = 1
    cycle = 0
    res = 0

    for line in data.splitlines():
        if line == "noop":
            cycle += 1
        else:
            val = int(line.split()[1])
            cycle += 2
            tube += val
        if (cycle % 40 == 20) or (cycle % 40 == 21 and line != "noop"):
            if line == "noop":
                res += cycle * tube
                print("noop", cycle * tube)
            else:
                if cycle % 20 == 0:
                    res += cycle * (tube - val)
                    print("addx", cycle * (tube - val))
                else:
                    res += (cycle - 1) * (tube - val)
                    print("addx last", (cycle - 1) * (tube - val))
            print(cycle, tube, res)
        if cycle > 220:
            break

    return res


def solve_b(data):
    tube = 1
    position = 0

    lines = data.splitlines()

    rest = 0
    val = 0
    while lines:
        for i in range(rest):
            print("#" if tube - 1 <= position <= tube + 1 else " ", end="")
            position += 1
            if position % 40 == 0:
                position = 0
                print()
        tube += val
        line = lines.pop(0)
        if line == "noop":
            rest = 1
            val = 0
        else:
            rest = 2
            val = int(line.split()[1])
    print()


